def validate_user(username, password):
    """Valida credenciais do usu√°rio"""
    users = load_users()
    for user in users:
        if user['username'] == username and user['password'] == password:
            return True
    return False

def user_exists(username):
    """Verifica se usu√°rio j√° existe"""
    users = load_users()
    for user in users:
        if user['username'] == username:
            return True
    return False

