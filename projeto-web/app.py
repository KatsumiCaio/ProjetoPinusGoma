@app.route('/registrar-processo', methods=['GET', 'POST'])
def registrar_processo():
    """P√°gina para registrar novos processos"""
    if 'logged_in' not in session or not session['logged_in']:
        flash('üîí Voc√™ precisa fazer login primeiro!', 'error')
        return redirect(url_for('login'))
    
    if request.method == 'POST':
        # Coleta dados do formul√°rio
        numero_processo = request.form['numero_processo']
        tipo_processo = request.form['tipo_processo']
        descricao = request.form['descricao']
        localizacao = request.form['localizacao']
        area_hectares = request.form['area_hectares']
        
        # Valida√ß√£o b√°sica
        if not all([numero_processo, tipo_processo, descricao, localizacao, area_hectares]):
            flash('‚ùå Todos os campos s√£o obrigat√≥rios!', 'error')
            return render_template('registrar_processo.html')
        
        # Carrega processos existentes
        processos = load_processos()
        
        # Verifica se o n√∫mero do processo j√° existe
        for processo in processos:
            if processo['numero_processo'] == numero_processo:
                flash('‚ùå N√∫mero de processo j√° existe!', 'error')
                return render_template('registrar_processo.html')
        
        # Cria novo processo
        novo_processo = {
            'id': len(processos) + 1,
            'numero_processo': numero_processo,
            'tipo_processo': tipo_processo,
            'descricao': descricao,
            'localizacao': localizacao,
            'area_hectares': float(area_hectares),
            'data_registro': datetime.now().strftime('%d/%m/%Y %H:%M:%S'),
            'usuario_registro': session['username'],
            'status': 'Em An√°lise'
        }
        
        # Adiciona √† lista e salva
        processos.append(novo_processo)
        if save_processos(processos):
            flash('‚úÖ Processo registrado com sucesso!', 'success')
            return redirect(url_for('menu'))
        else:
            flash('‚ùå Erro ao salvar processo!', 'error')
    
    return render_template('registrar_processo.html')

@app.route('/cadastrar-usuario', methods=['GET', 'POST'])
def cadastrar_usuario():
    """P√°gina para cadastrar novos usu√°rios"""
    if 'logged_in' not in session or not session['logged_in']:
        flash('üîí Voc√™ precisa fazer login primeiro!', 'error')
        return redirect(url_for('login'))
    
    if request.method == 'POST':
        # Coleta dados do formul√°rio
        username = request.form['username'].strip()
        password = request.form['password']
        confirm_password = request.form['confirm_password']
        nome_completo = request.form['nome_completo'].strip()
        email = request.form['email'].strip()
        
        # Valida√ß√£o b√°sica
        if not all([username, password, confirm_password, nome_completo, email]):
            flash('‚ùå Todos os campos s√£o obrigat√≥rios!', 'error')
            return render_template('cadastrar_usuario.html')
        
        # Verifica se as senhas coincidem
        if password != confirm_password:
            flash('‚ùå As senhas n√£o coincidem!', 'error')
            return render_template('cadastrar_usuario.html')
        
        # Verifica se o usu√°rio j√° existe
        if user_exists(username):
            flash('‚ùå Nome de usu√°rio j√° existe!', 'error')
            return render_template('cadastrar_usuario.html')
        
        # Carrega usu√°rios existentes
        users = load_users()
        
        # Cria novo usu√°rio
        novo_usuario = {
            'username': username,
            'password': password,
            'nome_completo': nome_completo,
            'email': email,
            'data_cadastro': datetime.now().strftime('%d/%m/%Y %H:%M:%S'),
            'cadastrado_por': session['username']
        }
        
        # Adiciona √† lista e salva
        users.append(novo_usuario)
        if save_users(users):
            flash('‚úÖ Usu√°rio cadastrado com sucesso!', 'success')
            return redirect(url_for('menu'))
        else:
            flash('‚ùå Erro ao salvar usu√°rio!', 'error')
    
    return render_template('cadastrar_usuario.html')

